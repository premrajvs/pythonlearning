# Scenario: Storing a user's name and age.
name = "John"
age = 38
print(f"Hello, my name is {name} and I am {age} years old.")
# you may get a question of why use the variable f instead the curved bracket.
# It is called f-string. This allows you to embed string expression (name and age variables)
# within the string literals.
